    </div>
  </div>
</template>

<script>
export default {
  data:() => ({
    show: false,
  }),
  mounted() {
    this.$cookiz.remove('access_token_t')
    this.$cookiz.remove('access_token_t', {
      path: '/',
      domain: '.dapengjiaoyu.cn'
    })
    this.$cookiz.remove('access_token', {
      path: '/',
      domain: '.dapengjiaoyu.cn'
    })

    
    /*** 
     * 【刷新或者首次加载】
     * 等待文档树渲染完毕后，再放开显示
    */
    this.$nextTick(() => {
      this.show = true
    })
  }
}
</script>


