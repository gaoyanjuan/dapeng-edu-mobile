<template>
  <div>
    <div class="app_wrap" v-if="$nuxt.isOnline && show">
      <Nuxt />
    </div>
    <div class="off-line-wrap" v-if="$nuxt.isOffline">
       <span class="off-line-txt"> 您的网络已断开 </span>
